#include <unistd.h>

char	*ft_strcat(char *dest, char *src)
{
	char	*home;

	home = dest;
	while (*dest)
	{
		dest++;
	}
	while (*src)
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (home);
}
